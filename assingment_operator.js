// Declare a variable
let x = 10;
console.log("Initial value:", x);

// Assignment Operators
x += 5;  // Addition assignment
console.log("After x += 5:", x);

x -= 3;  // Subtraction assignment
console.log("After x -= 3:", x);

x *= 2;  // Multiplication assignment
console.log("After x *= 2:", x);

x /= 4;  // Division assignment
console.log("After x /= 4:", x);

x %= 3;  // Modulus assignment
console.log("After x %= 3:", x);

x **= 2; // Exponentiation assignment
console.log("After x **= 2:", x);

// Bitwise Assignment Operators
x = 5;   // Reset x to 5
x &= 3;  // Bitwise AND assignment
console.log("After x &= 3:", x);

x |= 2;  // Bitwise OR assignment
console.log("After x |= 2:", x);

x ^= 1;  // Bitwise XOR assignment
console.log("After x ^= 1:", x);

x <<= 1; // Left shift assignment
console.log("After x <<= 1:", x);

x >>= 1; // Right shift assignment
console.log("After x >>= 1:", x);

x >>>= 1; // Unsigned right shift assignment
console.log("After x >>>= 1:", x);